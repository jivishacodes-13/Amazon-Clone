// Get products by category
export const getProductsByCategory = (categoryName) => {
  return allProducts.filter(product => product.category === categoryName);
};

// Search products
export const searchProducts = (query) => {
  const searchTerm = query.toLowerCase();
  return allProducts.filter(product =>
    product.title.toLowerCase().includes(searchTerm) ||
    product.description.toLowerCase().includes(searchTerm) ||
    product.category.toLowerCase().includes(searchTerm)
  );
};

// Get best sellers (products with highest rating)
export const getBestSellers = () => {
  return [...allProducts].sort((a, b) => b.rating - a.rating).slice(0, 8);
};

// Get today's deals (products with price < 1000)
export const getTodaysDeals = () => {
  return allProducts.filter(product => product.price < 1000).slice(0, 8);
};

// Get product by ID
export const getProductById = (id) => {
  return allProducts.find(product => product.id === id);
};
